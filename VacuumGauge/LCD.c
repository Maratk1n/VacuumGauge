void LCD_busy_flag_check()
{
	_delay_us(40);
}

void LCD_command_write(uint8_t DB)
{
	PORTG &= ~_BV(0);
	PORTC = DB;
	PORTG |= _BV(1);
	__nop();
	PORTG &= ~_BV(1);
	PORTC = 0;
}

void LCD_data_write(uint8_t DB)
{
	PORTG |= _BV(0);	// RS signal
	PORTC = DB;
	PORTG |= _BV(1);    // E strobe
	__nop();
	PORTG &= ~_BV(1);    // E strobe
	PORTC = 0;
	PORTG &= ~_BV(0);    // RS signal
}

void LCD_init()
{
	_delay_ms(15);
	LCD_command_write(0x30);
	_delay_ms(5);
	LCD_command_write(0x30);
	_delay_ms(2);
	LCD_command_write(0x30);
	_delay_ms(2);
	LCD_command_write(0x38);
	_delay_us(40);
	LCD_command_write(0xC);
	_delay_us(40);
	LCD_command_write(0x40);
	_delay_us(40);
	
	for(uint8_t i = 0; i < 0x40; i++){
		LCD_data_write(User[i]);
		_delay_us(40);
	}

	LCD_command_write(0x1);
	_delay_ms(2);
}

void LCD_text_out(uint8_t const* topline, uint8_t const* botline)
{
	LCD_busy_flag_check();
	LCD_command_write(0x80);
	uint8_t eol = 0;
	for(uint8_t i = 0; i < 16; i++)
	{
		LCD_busy_flag_check();
		if (!*topline)
		eol = 1;
		if (!eol)
		LCD_data_write(Cyrillic[*topline++]);
		else
		LCD_data_write(32);
	}
	LCD_busy_flag_check();
	LCD_command_write(0xC0);
	eol = 0;
	for(uint8_t i = 0; i < 16; i++)
	{
		LCD_busy_flag_check();
		if (!*botline)
		eol = 1;
		if (!eol)
		LCD_data_write(Cyrillic[*botline++]);
		else
		LCD_data_write(32);
	}
}

void LCD_masked_text(uint8_t const* topline, uint8_t const* botline, uint16_t topmask, uint16_t botmask)
{
	LCD_busy_flag_check();
	LCD_command_write(0x80);
	uint8_t eol = 0;
	for(uint8_t i = 0; i < 16; i++)
	{
		LCD_busy_flag_check();
		if (!*topline)
			eol = 1;
		if (eol||(topmask&_BV(i)))
			LCD_data_write(32);
		else
			LCD_data_write(Cyrillic[*topline]);
		topline++;
	}
	LCD_busy_flag_check();
	LCD_command_write(0xC0);
	eol = 0;
	for(uint8_t i = 0; i < 16; i++)
	{
		LCD_busy_flag_check();
		if (!*botline)
			eol = 1;
		if (eol||(botmask&_BV(i)))
			LCD_data_write(32);
		else
			LCD_data_write(Cyrillic[*botline]);
		botline++;
	}
}

void Display(void *t, void *b, uint16_t tmsk, uint16_t bmsk)
{
	LCD_masked_text(t, b,tmsk ,bmsk);
}